        while (i >= 0 && j >= 0) {
            if (nums1[i] > nums2[j]) {
                nums1[p] = nums1[i];
                i--;
            } else {
                nums1[p] = nums2[j];
                j--;
            }
            p--;
        }
        // å¯èƒ½å…¶ä¸­ä¸€ä¸ªæ•°ç»„çš„æŒ‡é’ˆèµ°åˆ°å°½å¤´äº†ï¼Œè€Œå¦ä¸€ä¸ªè¿˜æ²¡èµ°å®Œ
