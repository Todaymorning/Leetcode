class Solution_M1 {
public:
    void merge(vector<int>& nums1, int m, vector<int>& nums2, int n) {
        vector<int> merges(nums1.size());
        int idx1 = 0, idx2 = 0;
        for(int i=0; i<nums1.size(); i++) {
            if(idx1 >= m) {
                merges[i] = nums2[idx2++];
            }
            else if(idx2 >= n) {
                merges[i] = nums1[idx1++];
            }
            else if(nums1[idx1] < nums2[idx2]) {
                merges[i] = nums1[idx1++];
            }
            else{
                merges[i] = nums2[idx2++];
            }
        }
        for(int i=0; i<nums1.size();i++) {
            nums1[i] = merges[i];
        }
    }
};

